--[[
    This user patch allows the user to have a more granular control of what to show in list views:

    - progress bar
    - progress percentage
    - pages: total number of pages when book is new or finished, 
        pages left to read when book is on hold, 
        page progress (read no. of total no.) when reading,
    - file info: filetype and size
    Each of the above can be selected independently.

    This is now separate from the settings for Cover Grid:
    - progress bar (default)
    - filesize
    - page progress
    - percent progress
    Only one of the above can be active at a time.

    Also changed series display to the format:
    Book NUMBER_IN_SERIES of SERIES_NAME

]]--